"""
Обработчик команды /help.
Показывает справку по использованию бота.
"""

from telegram import Update
from telegram.ext import ContextTypes


async def help_command(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """
    Обработчик команды /help.
    Показывает список доступных команд и инструкции.
    """
    
    help_text = """
🎤 *Vocal Tutor — Справка*

*Команды:*
/start — Начать работу, выбрать тип голоса
/exercise — Выбрать упражнение для тренировки
/progress — Посмотреть свой прогресс
/settings — Настройки (тип голоса)
/help — Эта справка

*Как пользоваться:*

1️⃣ Выбери упражнение через /exercise
2️⃣ Запиши голосовое сообщение (пой упражнение)
3️⃣ Отправь его мне
4️⃣ Получи анализ и советы от AI

*Советы для лучшего результата:*
• Записывай в тихом месте 🤫
• Держи телефон на расстоянии 20-30 см
• Пой чётко и не спеши
• Записи до 30 секунд работают лучше всего

*Обозначения в результатах:*
✅ — Отлично (отклонение <20 центов)
⚠️ — Неплохо (отклонение 20-50 центов)  
❌ — Нужно поработать (>50 центов)

*Что такое центы?*
1 полутон = 100 центов. Человеческое ухо замечает разницу от 10-15 центов.

💡 *Совет:* Регулярные короткие тренировки (5-10 минут) эффективнее длинных редких!
"""
    
    await update.message.reply_text(
        help_text,
        parse_mode="Markdown"
    )
